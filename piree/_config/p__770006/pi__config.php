					//===============================================
					// 시작 => 게시판_코드__있으면
					IF ($bo_table)
					{

							//===========================================
							// 게시판_에_게시물_수__파악
							// $sql = "SELECT COUNT(*) FROM `".$write_table."` WHERE wr_is_comment='0' AND wr_pi_report_t=0";
							$sql = "SELECT COUNT(*) FROM `".$write_table."` WHERE wr_is_comment='0'";
							$article_t = sql_efv($sql);


							//===========================================
							// 게시판에_게시물_수__새로고침
							$sql = "UPDATE `".$g5['board_table']."` SET bo_count_write='".$article_t."' WHERE bo_table='".$bo_table."'";
							sql_query ($sql);

					}
					// 끝 => 게시판_코드__있으면
					//===============================================

				BREAK;
				###################################################
				# 끝 => 게시물
				###################################################



				###################################################
				# 시작 => 댓글
				###################################################
				CASE "comment" :

					//===============================================
					// 댓글_의_원_게시물
					global $wr_parent;


					//===============================================
					// 시작 => 게시판_코드__원_게시물__있으면
					IF ($bo_table && $wr_parent > 0)
					{

							//===========================================
							// 게시물_에_댓글_수__파악
							// $sql = "SELECT COUNT(*) FROM `".$write_table."` WHERE wr_parent='".$wr_parent."' AND wr_is_comment='1' AND wr_pi_report_t=0";
							$sql = "SELECT COUNT(*) FROM `".$write_table."` WHERE wr_parent='".$wr_parent."' AND wr_is_comment='1'";
							$comment_t = sql_efv($sql);


							//===========================================
							// 게시물_에_댓글_수__새로고침
							$sql = "UPDATE `".$write_table."` SET wr_comment='".$comment_t."' WHERE wr_id='".$wr_parent."'";
							sql_query ($sql);

					}
					// 끝 => 게시판_코드__원_게시물__있으면
					//===============================================

				BREAK;
				###################################################
				# 끝 => 댓글
