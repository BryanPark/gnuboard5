	function get_report_row_url($get_type, $div_c, $bo_table="", $wr_id=0, $parent_n=0, $mb_id="")
	{

		//=====================================================
		// 전역_변수
		global $g5;


		//=====================================================
		// 넘겨줄_변수
		$report_link_arr = array();


		//=====================================================
		// 시작 => 넘겨줄_URL__구분__기본값
		IF ($get_type != 'admin_url')
		{
			$get_type = 'user_url';
		}
		// 끝 => 넘겨줄_URL__구분__기본값
		//=====================================================


		//=====================================================
		// 시작 => 프로그램__구분
		SWITCH ($div_c)
		{

			#####################################################
			# 시작 => 게시물
			CASE "article" :

				// 사용자_링크
				$report_link_arr["user_url"] = G5_BBS_URL."/board.php?bo_table=".$bo_table."&wr_id=".$wr_id;

				// 관리자_링크
				$report_link_arr["admin_url"] = $report_link_arr["user_url"];

			BREAK;
			# 끝 => 게시물
			#####################################################


			#####################################################
			# 시작 => 댓글
			CASE "comment" :

				// 사용자_링크
				$report_link_arr["user_url"] = G5_BBS_URL."/board.php?bo_table=".$bo_table."&wr_id=".$parent_n."#c_".$wr_id;

				// 관리자_링크
				$report_link_arr["admin_url"] = $report_link_arr["user_url"];

			BREAK;
			# 끝 => 댓글
			#####################################################


			#####################################################
			# 시작 => 회원
			CASE "member" :

				// 사용자_링크
				$report_link_arr["user_url"] = G5_BBS_URL."/profile.php?mb_id=".$mb_id;

				// 관리자_링크
				$report_link_arr["admin_url"] = G5_ADMIN_URL."/member_form.php?w=u&mb_id=".$mb_id;

			BREAK;
			# 끝 => 회원
			#####################################################

		}
		// 끝 => 프로그램__구분
		//=====================================================


		//=====================================================
		// 넘겨주기
		return $report_link_arr[$get_type];

	}
